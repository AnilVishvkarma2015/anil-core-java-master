package com.corejava.controlstatement;

/**
 * Example class for FOR loop
 * 
 * @version 1.0
 * @since 16 Nov 2016
 * @author Anil Vishvkarma
 */

public class ForLoop {

	public static void main(String[] args) {

		// Print Hello 5 times
		for (int i = 0; i < 5; i++) {
			System.out.println(i + " Hello For Loop");
		}

	}
}
